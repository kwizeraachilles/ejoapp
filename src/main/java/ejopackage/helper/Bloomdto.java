package ejopackage.helper;

public class Bloomdto {
	private  String name;
	private float remembering;
	private float understanding;
	private float applying;
	private float analyzing;

	private float evaluating;
	private float  creating;
	public Bloomdto() {
	
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public float getRemembering() {
		return remembering;
	}
	public void setRemembering(float remembering) {
		this.remembering = remembering;
	}
	public float getUnderstanding() {
		return understanding;
	}
	public void setUnderstanding(float understanding) {
		this.understanding = understanding;
	}
	public float getApplying() {
		return applying;
	}
	public void setApplying(float applying) {
		this.applying = applying;
	}
	public float getAnalyzing() {
		return analyzing;
	}
	public void setAnalyzing(float analyzing) {
		this.analyzing = analyzing;
	}
	public float getEvaluating() {
		return evaluating;
	}
	public void setEvaluating(float evaluating) {
		this.evaluating = evaluating;
	}
	public float getCreating() {
		return creating;
	}
	public void setCreating(float creating) {
		this.creating = creating;
	}
	
	

}
