package ejopackage.helper;

public class Coursecomplete {

	private int id;

	private String name;
	
    private int level;
    private int hourz;
    private int importance;
	public Coursecomplete() {
	
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getHourz() {
		return hourz;
	}
	public void setHourz(int hourz) {
		this.hourz = hourz;
	}
	public int getImportance() {
		return importance;
	}
	public void setImportance(int importance) {
		this.importance = importance;
	}

}
